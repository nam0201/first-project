package java_1027;

public class Comp_Op {

	public static void main(String[] args) {
		// 비교연산자 :  > , <  , == , != , <= , >=  결과값이  true, false

		int a=100;
		int b=90;
		
		System.out.println(a>b); // 100 > 90
		System.out.println(a<b); // 100 < 90
		System.out.println(a==b); // 100 == 90
		System.out.println(a!=b); // 100 != 90
		System.out.println(a<=b); // 100 <= 90
		System.out.println(a>=b); // 100 >= 90
	}

}
