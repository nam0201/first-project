package java_1027;

public class If_Ex2{

	public static void main(String[] args) {
		 
		int money=10;
		
		// 돈이 10만원 이상이면 들어가겠다
		
		if( money < 100000 )
		{  // 조건이 true일때
			System.out.println("다음에 가자");
		}
		else
		{  // 조건이 false일때
			System.out.println("초밥집에 들어가자");
		}
	}

}
