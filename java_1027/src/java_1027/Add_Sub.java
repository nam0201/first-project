package java_1027;

public class Add_Sub {

	public static void main(String[] args) {
		// 증감 연산자
		int a=100;
		
		// a의 값을 1 증가시키기
		a++; // a=a+1;  //  = 를 기준으로 오른쪽이 먼저 실행된 후 값을 왼쪽에 준다
		
		System.out.println(a);

		
		int b=100;
		// b의 값을 1 감소시키기
		b--; // b=b-1;
		
		System.out.println(b);
	}

}
