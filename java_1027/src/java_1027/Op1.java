package java_1027;

public class Op1 {

	public static void main(String[] args) {
		 // 산술연산자 : + - * / %

		int a=100;
		int b=40;
		
		System.out.println(a+b);  // (100+40)
		System.out.println(a-b);  // (100-40)
		System.out.println(a*b);  // (100*40)
		System.out.println(a/b);  // (100/40)
		System.out.println(a%b);  // (100%40)
	}

}
